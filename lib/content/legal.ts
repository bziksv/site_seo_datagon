function insertBeforeLegalRequisites(html: string, clauseHtml: string): string {
  const re = /(?:<br\s*\/?>\s*)*<p>\s*Юридические реквизиты/i;
  if (re.test(html)) {
    return html.replace(re, `${clauseHtml}<p> Юридические реквизиты`);
  }
  return html + clauseHtml;
}

function appendOfferRetentionClause(html: string): string {
  if (html.includes("13.6. Для Клиентов на бесплатном тарифе")) {
    return html;
  }
  return insertBeforeLegalRequisites(html, OFFER_FREE_TARIFF_RETENTION_CLAUSE);
}

function appendOfferInactiveAccountClause(html: string): string {
  if (html.includes("13.7. Удаление учётной записи Клиента при длительной неактивности")) {
    return html;
  }
  return insertBeforeLegalRequisites(html, OFFER_INACTIVE_ACCOUNT_DELETION_CLAUSE);
}

/**
 * Убирает артефакты вёрстки/скрейпа оферты:
 * лишние <br> между блоками (дают двойной вертикальный зазор поверх margin у p/ul),
 * пустые хвосты, мелочи вроде «;;» и склеенных пунктов.
 */
function normalizeOfferParagraphBreaks(html: string): string {
  return (
    html
      // Любое число <br> между блочными тегами → соседние блоки без пустой строки.
      .replace(/<\/(p|ul|ol)>(?:\s*<br\s*\/?>)+\s*<(p|ul|ol)>/gi, "</$1><$2>")
      .replace(/^(?:\s*<br\s*\/?>)+/i, "")
      .replace(/(?:<br\s*\/?>\s*)+$/i, "")
      // Склеенные 7.3 и 7.4 в одном <p>.
      .replace(
        /(\.\s*)(7\.4\.\s+При использовании Сервиса запрещаются)/gi,
        "$1</p><p> $2",
      )
      // Склеенные 8.7 и 8.8.
      .replace(
        /(в Сервисе\.\s*)(8\.8\.\s+Безопасность персональных данных)/gi,
        "$1</p><p> $2",
      )
      .replace(/;;+/g, ";")
      .replace(/8\.\s+1\./g, "8.1.")
  );
}

/** Старые /legal/personal-data и /legal/privacy редиректят на privacy-policy (next.config). */
export function getAllLegalSlugs(): string[] {
  return ["offer"];
}

export function getLegalBySlug(slug: string): LegalDoc | undefined {
  const doc = LEGAL_DOCS.find((d) => d.slug === slug);
  if (!doc) return undefined;
  let bodyHtml = localizeLegalHtml(doc.bodyHtml);
  if (slug === "offer") {
    bodyHtml = appendOfferRetentionClause(bodyHtml);
    bodyHtml = appendOfferInactiveAccountClause(bodyHtml);
    bodyHtml = normalizeOfferParagraphBreaks(bodyHtml);
  }
  return { ...doc, bodyHtml };
}

