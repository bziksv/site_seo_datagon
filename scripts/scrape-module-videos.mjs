/**
 * Сбор YouTube embed с лендингов модулей на redbox.su
 * Запуск: node scripts/scrape-module-videos.mjs
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const MODULES_TS = path.join(__dirname, "../lib/content/modules.ts");
const OUT = path.join(__dirname, "../lib/content/module-videos.generated.ts");
const SITE = "https://redbox.su";

const paths = [...fs.readFileSync(MODULES_TS, "utf8").matchAll(/path: "(\/[^"]+\/)"/g)].map(
  (m) => m[1]
);

function extractYoutube(html) {
  const urls = new Set();
  for (const m of html.matchAll(/youtube\.com\/embed\/([a-zA-Z0-9_-]+)/g)) {
    urls.add(`https://www.youtube.com/embed/${m[1]}`);
  }
  return [...urls];
}

const map = {};

for (const p of paths) {
  const slug = p.replace(/^\/|\/$/g, "");
  const url = `${SITE}${p}`;
  const html = await (await fetch(url)).text();
  const videos = extractYoutube(html);
  if (videos.length) map[slug] = videos;
  console.log(slug, videos.length);
  await new Promise((r) => setTimeout(r, 150));
}

const lines = [
  "/** AUTO-GENERATED by scripts/scrape-module-videos.mjs */",
  "export const MODULE_VIDEOS: Record<string, string[]> = {",
];
for (const [slug, videos] of Object.entries(map)) {
  lines.push(`  ${JSON.stringify(slug)}: ${JSON.stringify(videos)},`);
}
lines.push("};", "");

fs.writeFileSync(OUT, lines.join("\n"));
console.log("written", OUT, Object.keys(map).length, "modules with video");
